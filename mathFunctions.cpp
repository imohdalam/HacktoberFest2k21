#include <bits/stdc++.h>
using namespace std;

int main()
{
	cout<<"Sin(180)="<<sin(180)<<endl;
	cout<<"Cos(180)="<<cos(180)<<endl;
	cout<<"Tan(180)="<<tan(180)<<endl;
	cout<<"e^10="<<exp(10)<<endl;
	cout<<"log10(100)="<<log10(100)<<endl;
	cout<<"log(100)="<<log(100)<<endl;
	cout<<"Ceiling of log(100)="<<ceil(log(100))<<endl;
	cout<<"Floor of log(100)="<<floor(log(100))<<endl;
	
	return 0;
}
